<p>Most Sweet 16's follow the timeline below. It may vary depending on the activities you have planned, the venue, and the way the evening runs (you might want more dance music than planned or eliminate a Candle Lighting Ceremony).  Below is a sample timeline of a party beginning at 8pm.</p>
<p><strong>8:00 to 8:20PM</strong></p>
<p>Affair begins and guests arrive. At this time guests might be getting their table cards and locating their seats, signing a special message to the birthday girl/boy, or just mingling with other guests.  Depending on the type of affair and guests, we will be playing either current radio hits (if the party is dominated by a younger crowd or if it is less formal) or cocktail music (if the party is more formal).</p>
<p><strong>8:20 to 9:00PM</strong></p>
<p>If requested, this time is used for the grand entrance of the parents and any other special family members and finally, the Sweet 16 girl/boy! This is usually followed by a dance with the father or mother of the Sweet 16 girl/boy.  Parents of the birthday girl/boy may decide to use this time to give a short speech or thank all guests for coming.</p>
<p>The main dance floor will then be opened for the first music set.  Depending on the guests and client’s request, this set can be used to welcome older family members onto the floor playing music from the 1950's-1970's or to generate the younger crowd’s energy and play current and contemporary dance music.</p>
<p><strong>9:00 to 9:45PM</strong></p>
<p>Dinner is typically served during this time.  The M.C. will ask the guests to kindly find their seats. If the affair has sit-down dinner, it will be served.  If there is a buffet, we will call the tables up to the buffet in numerical order starting with the dais and parent tables.</p>
<p><strong>9:45 to 10:30PM</strong></p>
<p>As guests finish their dinner, we will then open the dance floor for the second music set including a variety of styles, dependant on the crowd.  If they happen to like one particular style, we will play more of that style of music.  This set will also include any popular party dances (if requested), such as the Dollar Dance and the Cha-Cha Slide.  The conclusion of this set may be used to view a special video montage in honor of the birthday girl/boy.</p>
<p><strong>10:30 to 11:15PM</strong></p>
<p>The Sweet 16 candle ceremony (if requested) will begin at this point. The candle ceremony typically runs for approximately 30 to 45 minutes.  After the candles are lit, the guests will sing &quot;Happy Birthday&quot;, led by the M.C. to the birthday girl/boy.  Cake, coffee, and desserts will be served at the conclusion of the candle ceremony.</p>
<p><strong>11:15PM to 12:00AM</strong></p>
<p>The final dance segment of the evening consists of the most recent and high energy dance music depending on the affair and your preferences.  The evening ends with our M.C. thanking the guests and wishing them well on behalf of you and <em>Lucky in Love DJ Productions</em>.</p>
