			<h2>Sample Itinerary</h2>
<p>Here is an example of an itinerary for a four-hour reception that begins at 7:00pm:</p>
<p><strong>First Hour of Reception</strong></p>
<p><strong>7:00</strong>  Cocktail hour begins.  Guests enjoy open bar and hors d’oeuvres, appetizers and possibly stations.  Bridal party may use this time to relax in bridal suite, take pictures, or bride and groom can welcome and thank guests for coming.</p>
<p><strong>7:50</strong>  Cocktail hour comes to an end.  Guests are invited to reception room and are seated at assigned tables.</p>
<p><strong>Second Hour of Reception</strong></p>
<p><strong>8:00</strong> 	Bride and Groom, wedding party and parents of bride and groom gather in side room to "line up" for announcements.</p>
<p>Line up in order... groom's parents first, then bride's parents, flower girl and ring bearer, bridesmaids escorted by ushers, maid of honor escorted by best man and then the bride and groom.</p>
<p><strong>8:05</strong> 	DJ will make formal announcements introducing bridal party.</p>
<p>For a unique way to announce your bridal party, customize your wedding announcements with <em>Lucky in Love</em>!</p>
<p><strong>8:10</strong> 	Bride and Groom's first dance.  Caterer begins serving champagne to the tables for the champagne toast.  If requested, bridal party (and guests) can be invited to join in bride/groom first dance or a second song.</p>
<p><strong>8:20</strong>   DJ to announce that dinner is served and ask that all guests be seated (first course may be distributed).</p>
<p>DJ introduces Officiant (or other designated person) who will say the blessing if requested.</p>
<p><strong>8:25</strong> 	DJ introduces the Best Man who will give the toast to the bride and groom using a wirleless mic.  If requested, maid of honor can also perform a toast prior to the best man’s toast.</p>
<p><strong>8:30</strong> 	Main course is served.</p>
<p><strong>Third Hour of Reception</strong></p>
<p><strong>9:00</strong> 	Bride &amp; Groom cut the cake.</p>
<p><strong>9:15</strong> 	DJ announces Bride to dance with her father (or other special relative).</p>
<p><strong>9:25</strong> 	DJ announces Groom to dance with his mother (or other special relative).</p>
<p><strong>9:35</strong> 	This time may be used for special moments such as bride and groom thank you to guests, anniversary or dollar dances, or viewing video montages.</p>
<p><strong>9:45</strong> 	Cake and coffee are served to guests.</p>
<p><strong>Fourth Hour of Reception</strong></p>
<p><strong>10:00</strong> 	DJ announces the Bride's bouquet toss.</p>
<p>A nice alternative to the bouquet toss is for the Bride to &quot;dedicate her bouquet&quot; to someone special - maybe her mother or best friend. The bride can say a few words and then give (not throw) her bouquet away. This is a very tasteful and sentimental alternative... and be prepared for a few tears.</p>
<p><strong>10:15</strong> Garter toss</p>
<p><strong>10:25</strong> Last set of dance music</p>
<p><strong>10:55</strong> Last dance</p>
<p>&nbsp;</p>
<p><strong>TIMING HINTS:</strong></p>
<p>A) You can start the dancing anytime after the entire bridal party and family have eaten.</p>
<p>B) Cut the cake 30-45 Minutes after the start of dancing, Let them get warmed up and dance awhile. This then serves as a good &quot;dessert and coffee time.&quot;</p>
<p>C) I suggest that you do the garter &amp; bouquet tosses right after the cake cutting. Otherwise everyone usually stands around and watches the caterers slice up the cake and they won't dance again till they get their cake. The bouquet toss clears the dance floor and saves time.</p>
<p>D) If you are planning to do a Money dance, doing it immediately after the cake cutting or garter toss assures that most of your guest will still be present. (The earlier the better)</p>
<p>(It also helps to have a couple of people pass out pins or lay them at the end of the tables.)</p>
<p>E) If you are going to change your clothes prior to leaving, do so about one-half hour prior to the end. Then you can have your Farewell dance about 15 Minutes prior to leaving so that we can play a couple of additional dance number for your guests while you are saying your good-byes.</p>
<p><em>From the announcement into the hall, to the bouquet &amp; garter ceremonies, timing is generally about two hours. If this is going to be a four hour wedding reception, this leaves you about two hours for dancing (three hours for a five hour reception).</em></p>

